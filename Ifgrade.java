class  Ifgrade
{
	public static void main(String[] args)
	{
		int s=52;
		if(s>=90) System.out.println("Your grade is: A");
		else if (s>=70) System.out.println("Your grade is: B");
		else if (s>=60) System.out.println("Your grade is: C");
		else if (s>=50) System.out.println("Your grade is: D");
		else System.out.println("Your grade is: F");
	}
}                               
       